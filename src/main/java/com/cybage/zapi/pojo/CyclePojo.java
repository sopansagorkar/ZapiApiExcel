package com.cybage.zapi.pojo;

public class CyclePojo {
	String versionID;
	String projectId;
	String cloneCycleID;

	public String getVersionID() {
		return versionID;
	}

	public void setVersionID(String versionID) {
		this.versionID = versionID;
	}

	public String getProjectId() {
		return projectId;
	}

	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	public String getCloneCycleID() {
		return cloneCycleID;
	}

	public void setCloneCycleID(String cloneCycleID) {
		this.cloneCycleID = cloneCycleID;
	}
}
